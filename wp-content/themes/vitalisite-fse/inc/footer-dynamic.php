/**
 * Replace the footer template-part with a fully rendered footer.
 */
function render_dynamic_footer( $block_content, $block ) {
	if ( 'core/template-part' !== $block['blockName'] ) {
		return $block_content;
	}
	if ( empty( $block['attrs']['slug'] ) || 'footer' !== $block['attrs']['slug'] ) {
		return $block_content;
	}

	$cabinet = get_option( 'vitalisite_cabinet', array() );
	$social  = get_option( 'vitalisite_social', array() );

	$contact_col = build_footer_contact_column( $cabinet );
	$social_html = build_footer_social_links( $social );
	$site_name   = get_bloginfo( 'name' );
	$year        = gmdate( 'Y' );

	ob_start();
	?>
	<footer class="vitalisite-footer" role="contentinfo">
		<div class="vitalisite-footer__inner">

			<div class="vitalisite-footer__columns">
				<div class="vitalisite-footer__col vitalisite-footer__col--brand">
					<p class="vitalisite-footer__site-name"><?php echo esc_html( $site_name ); ?></p>
					<p class="vitalisite-footer__tagline"><?php echo esc_html__( 'Un thème pensé pour inspirer confiance, avec des sections prêtes à l\'emploi et faciles à personnaliser.', 'vitalisite-fse' ); ?></p>
				</div>

				<div class="vitalisite-footer__col vitalisite-footer__col--nav">
					<p class="vitalisite-footer__heading"><?php echo esc_html__( 'Navigation', 'vitalisite-fse' ); ?></p>
					<ul class="vitalisite-footer__nav-list">
						<li><a href="<?php echo esc_url( home_url( '/' ) ); ?>"><?php echo esc_html__( 'Accueil', 'vitalisite-fse' ); ?></a></li>
						<li><a href="<?php echo esc_url( home_url( '/specialites' ) ); ?>"><?php echo esc_html__( 'Spécialités', 'vitalisite-fse' ); ?></a></li>
						<li><a href="<?php echo esc_url( home_url( '/contact' ) ); ?>"><?php echo esc_html__( 'Contact', 'vitalisite-fse' ); ?></a></li>
					</ul>
				</div>

				<?php if ( $contact_col ) : ?>
				<div class="vitalisite-footer__col vitalisite-footer__col--contact">
					<?php echo $contact_col; ?>
				</div>
				<?php endif; ?>
			</div>

			<div class="vitalisite-footer__bottom">
				<p class="vitalisite-footer__copyright">© <?php echo esc_html( $year ); ?> <?php echo esc_html( $site_name ); ?>. <?php echo esc_html__( 'Tous droits réservés.', 'vitalisite-fse' ); ?></p>
				<?php if ( $social_html ) : ?>
					<?php echo $social_html; ?>
				<?php endif; ?>
			</div>

		</div>
	</footer>
	<?php
	return ob_get_clean();
}
add_filter( 'render_block', __NAMESPACE__ . '\render_dynamic_footer', 10, 2 );

/**
 * Build the contact info HTML for the footer.
 */
function build_footer_contact_column( $cabinet ) {
	$show_phone   = ! empty( $cabinet['show_phone_footer'] );
	$show_email   = ! empty( $cabinet['show_email_footer'] );
	$show_address = ! empty( $cabinet['show_address_footer'] );

	$lines = array();

	if ( $show_phone && ! empty( $cabinet['phone'] ) ) {
		$phone      = esc_html( $cabinet['phone'] );
		$phone_href = preg_replace( '/[^0-9+]/', '', $cabinet['phone'] );
		$lines[]    = '<li><a href="tel:' . esc_attr( $phone_href ) . '">' . $phone . '</a></li>';
	}

	if ( $show_email && ! empty( $cabinet['email'] ) ) {
		$email   = esc_html( $cabinet['email'] );
		$lines[] = '<li><a href="mailto:' . esc_attr( $cabinet['email'] ) . '">' . $email . '</a></li>';
	}

	if ( $show_address && ! empty( $cabinet['address'] ) ) {
		$lines[] = '<li>' . esc_html( $cabinet['address'] ) . '</li>';
	}

	if ( empty( $lines ) ) {
		return '';
	}

	$html  = '<p class="vitalisite-footer__heading">' . esc_html__( 'Contact', 'vitalisite-fse' ) . '</p>';
	$html .= '<ul class="vitalisite-footer__contact-list">';
	$html .= implode( '', $lines );
	$html .= '</ul>';

	return $html;
}

